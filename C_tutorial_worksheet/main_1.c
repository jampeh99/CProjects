#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("Hello world!\n");
    printf("%d \n", 45);///integer
    printf("%ld \n", 746847);///large integer
    printf("%f \n",73.84);
    printf("%c \n", 'J');///character
    printf("%s \n", "Jeffrey");///string
    printf("%x \n", 59);///hexadecimal

    //Storing Values in Variables
    printf("Examples of variables \n");
    int age=18;
    float pi=3.142;
    char c='J';
    printf("Age %d.", age);

    return 0;
}
